export const createReaders = (data: any) => {
  return unitOfWork((transaction: Transaction) => {
    data = data.map((item: any, index: number) => {
      let obj = { ...item };
      if (index % 2 == 0) {
        obj.readerTypeId = 2;
        obj.civilServantId = obj.studentId;
        obj.studentId = null;
      }
      return obj;
    });
    return ReaderSchema.bulkCreate(data, { transaction });
  });
};
